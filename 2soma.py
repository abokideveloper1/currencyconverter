n1 = int(input("Enter first value: "))
n2 = int(input("Enter second value: "))
print("RESULT: {}".format(n1+n2))
print("RESULT: {:20}".format(n1+n2))
print("RESULT: {:<20}".format(n1+n2))
print("RESULT: {:>20}".format(n1+n2))
print("RESULT: {:^20}".format(n1+n2))
print("RESULT: {:=^20}".format(n1+n2))


